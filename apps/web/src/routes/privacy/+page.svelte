<h2 id="infocollect">1. WHAT INFORMATION DO WE COLLECT?</h2>

<h3>Personal information you disclose to us</h3>

<p><strong><em>In Short:</em></strong> <em>We collect personal information that you provide to us, and a minimal set of data necessary to operate the service.</em></p>

<p>We collect personal information that you voluntarily provide to us when you register on the Services, enable text chat, participate in activities on the Services, or otherwise when you contact us.</p>

<p><strong>Personal Information Provided by You.</strong> The personal information that we collect depends on the context of your interactions with us and the Services, the choices you make, and the features you use. The personal information we collect may include the following:</p>
<ul>
  <li><strong>Account identifier:</strong> A salted cryptographic hash of your Steam ID, generated on our server during authentication. This hash serves as your unique account identifier in our system. We do not store your raw Steam ID on our servers.</li>
  <li><strong>Display name / username:</strong> The display name you choose or that is derived from your Steam profile.</li>
  <li><strong>Discord user ID (optional):</strong> If you choose to link your Discord account, we store your Discord user ID to associate your game account with your Discord identity in our Discord server. We also generate and store a salted cryptographic hash of your Discord user ID for moderation purposes. You can unlink your Discord account at any time.</li>
  <li><strong>Date of birth:</strong> Collected only during the text chat activation process to verify you meet the minimum age requirement (16 years). Your date of birth is used for this one-time verification and is <strong>not stored</strong> after the check is completed. Only the result (eligible or ineligible) is recorded.</li>
  <li><strong>Chat messages:</strong> If you enable text chat, the content of messages you send within multiplayer lobbies is transmitted through and temporarily stored on our servers.</li>
  <li><strong>Moderation data:</strong> If your messages are flagged by our automated moderation system or reported by other users, the flagged message content, timestamps, moderation verdicts, and associated account identifier are stored as moderation records.</li>
  <li><strong>Hashed IP address:</strong> When you connect to the Services, your public IP address is immediately processed into a salted cryptographic hash using the same method as your account identifier. We store only this non-reversible hash. Your plaintext IP address is never stored on our servers. This hashed IP is used for moderation purposes, including detecting ban evasion and preventing abuse.</li>
</ul>

<p><strong>Sensitive Information.</strong> We do not process sensitive information.</p>

<p><strong>Steam Authentication Data.</strong> We use Steam authentication to create and manage your account. During this process, we receive your Steam ID from Valve Corporation. We immediately hash this identifier with a secret server-side salt and use the resulting hash as your account identifier. We do not access your Steam friends list, game library, purchase history, email address, or any other Steam account data. See Section 5 for more details.</p>

<p><strong>Local Storage.</strong> The mod stores your Steam ID locally on your device for authentication purposes (such as enabling auto-login). This data is stored on your device, not on our servers, and is under your control. You can delete it at any time by removing the mod's local configuration files.</p>

<p>All personal information that you provide to us must be true, complete, and accurate, and you must notify us of any changes to such personal information.</p>

