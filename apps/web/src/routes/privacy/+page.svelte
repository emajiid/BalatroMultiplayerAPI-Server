<h3>Steam Authentication</h3>

<p>Our Services use Steam authentication (provided by Valve Corporation) to create and manage your account. When you log in, we receive your Steam ID from Valve. We immediately process this ID by generating a salted cryptographic hash (SHA-256 with a secret server-side salt), and this hash becomes your account identifier in our system.</p>

<p>We do <strong>not</strong> access, collect, or store:</p>
<ul>
  <li>Your raw Steam ID (it is hashed immediately upon receipt and the original is not retained)</li>
  <li>Your Steam friends list</li>
  <li>Your Steam game library or purchase history</li>
  <li>Your email address associated with Steam</li>
  <li>Any other Steam account data beyond the ID needed for authentication</li>
</ul>

<p>Your relationship with Valve Corporation and your use of Steam is governed solely by your agreement(s) with Valve. We recommend that you review Valve's privacy policy to understand how they collect, use, and share your personal information.</p>

<h3>Discord Account Linking (Optional)</h3>

<p>You may optionally link your Discord account to your game account. Discord linking is <strong>not</strong> used for authentication or login. It is used solely to associate your game account with your Discord identity in our Discord server (for example, for role assignment or identity verification).</p>

<p>When you link your Discord account, we collect and store:</p>
<ul>
  <li><strong>Your Discord user ID:</strong> Stored alongside your game account while your account is active. Deleted when you unlink your Discord account or delete your game account.</li>
  <li><strong>A salted hash of your Discord user ID:</strong> Generated using the same salting and hashing process as your Steam ID. This hash is retained after account deletion for moderation and ban enforcement purposes (see <a href="#deletion">Section 8</a>).</li>
</ul>

<p>We do <strong>not</strong> access, collect, or store your Discord friends list, server memberships, message history, email address, or any other Discord account data beyond your user ID. Linking is voluntary and can be removed at any time through your account settings.</p>

<h2 id="chatdata">6. TEXT CHAT DATA AND CONTENT MODERATION</h2>

<p><strong><em>In Short:</em></strong> <em>Chat messages are processed by automated moderation systems, stored temporarily, and may be retained longer if flagged or reported.</em></p>

<p>If you opt in to the text chat feature (which requires verifying that you are at least 16 years of age), the following data processing occurs:</p>

<p><strong>Message processing:</strong> Every chat message you send is transmitted to our server and routed through Paxmod's content moderation API before being delivered to other lobby participants. Paxmod analyzes the text content for prohibited material and returns a moderation verdict. Messages that fail moderation may be blocked, censored, or flagged for review.</p>

<p><strong>What is stored per message:</strong></p>
<ul>
  <li>Your hashed account identifier (sender)</li>
  <li>Lobby identifier</li>
  <li>Message content (post-moderation)</li>
  <li>Timestamp</li>
  <li>Paxmod moderation verdict/scores</li>
  <li>Report flag (if reported by another user)</li>
</ul>

<p><strong>What is NOT stored:</strong></p>
<ul>
  <li>Your raw Steam ID</li>
  <li>Your plaintext IP address (your IP is immediately hashed upon connection; only the non-reversible hash is stored; see Section 1 for details)</li>
  <li>Your date of birth (used only for the one-time age check, then discarded)</li>
</ul>

<p><strong>User reports:</strong> When a user reports another user for abusive behavior, we log the reported message content, the reporter's account identifier, the reported user's account identifier, and a timestamp. False or malicious reports may result in action against the reporting user's account.</p>

<p><strong>CSAM reporting:</strong> We are registered with the National Center for Missing &amp; Exploited Children (NCMEC) CyberTipline. If we become aware of any content that constitutes or appears to constitute child sexual abuse material (CSAM) or child exploitation, we will report it to NCMEC and/or relevant law enforcement authorities and preserve all related evidence for a minimum of one year as required by 18 U.S.C. § 2258A (as amended by the REPORT Act).</p>

<p><strong>No expectation of privacy:</strong> Chat messages sent through the Services are subject to automated filtering and, where necessary, human review for moderation purposes. You should not expect privacy in the content of messages sent through the Services.</p>

