</ul>

<h2 id="infosafe">9. HOW DO WE KEEP YOUR INFORMATION SAFE?</h2>

<p><strong><em>In Short:</em></strong> <em>We aim to protect your personal information through a system of organizational and technical security measures.</em></p>

<p>We have implemented appropriate and reasonable technical and organizational security measures designed to protect the security of any personal information we process. These include:</p>
<ul>
  <li><strong>Identity and network hashing:</strong> Your Steam ID, Discord user ID (if linked), and IP address are all salted and hashed (SHA-256) before storage, preventing direct identification even in the event of a data breach. No plaintext versions of these identifiers are stored on our servers.</li>
  <li><strong>Secret salt protection:</strong> The salt used for hashing is stored as a server-side environment variable, never committed to source code or exposed to clients.</li>
  <li><strong>Encryption in transit:</strong> All communications between the game client and our servers use encrypted connections.</li>
  <li><strong>Data minimization:</strong> We collect only the minimum data necessary to operate the service, with short retention periods for most data types.</li>
  <li><strong>Pseudonymization at deletion:</strong> When accounts are deleted, retained moderation records are pseudonymized by replacing personal identifiers with opaque references.</li>
</ul>

<p>However, despite our safeguards and efforts to secure your information, no electronic transmission over the Internet or information storage technology can be guaranteed to be 100% secure, so we cannot promise or guarantee that hackers, cybercriminals, or other unauthorized third parties will not be able to defeat our security and improperly collect, access, steal, or modify your information. Although we will do our best to protect your personal information, transmission of personal information to and from our Services is at your own risk. You should only access the Services within a secure environment.</p>

